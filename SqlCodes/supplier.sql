CREATE TABLE Supplier (
    Supplier_ID INT PRIMARY KEY,
    Supplier_Name VARCHAR(100) NOT NULL,
    Phone_Number VARCHAR(15) NOT NULL,
    Email VARCHAR(100),
    Street VARCHAR(100),
    Area VARCHAR(100),
    Landmark VARCHAR(100),
    City VARCHAR(50),
    State VARCHAR(50),
    Postal_Code VARCHAR(10),
    Payment_Terms VARCHAR(100),
    Tax_ID VARCHAR(50)
);
